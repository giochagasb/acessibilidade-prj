          <h2 class="text-center fs-1 m-3">Autoritarismo</h2>
          <p>Enquanto que na democracia temos um regime marcado pela discordância, participação popular e conflito; em um Regime Autoritário temos constantemente, uma democracia de fachada onde ocorrem eleições, porém sem participação popular
            ou
            com sua
            participação fraldada.</p>
          <p>A ausência total ou parcial dos seguintes elementos básicos de uma democracia, pode indicar que o regime praticado é na verdade autoritário:</p>
          <ul>
            <li>Liberdade de expressão e de imprensa;</li>
            <li>Possibilidade de voto e elegibilidade política;</li>
            <li>Liberdade de associação política;</li>
            <li>Acesso à informação;</li>
            <li>Eleições idôneas.</li>
          </ul>
          <p>O processo que leva à perda desses direitos geralmente se inicia na própria democracia à medida em esta continua a falhar na tentativa de conciliar duas partes opostas levando a uma total intolerância entre ambas e, fatalmente, à
            instituição de um Regime Autoritário como resposta à parte derrotada, que passa a ser obrigada a se submeter.</p>
          <p>Por fim, vale ressaltar que o Autoritarismo pode se dar de diversas maneiras, como por exemplo: Ditadura, Totalitarismo, Fascismo, Nazismo, Monarquia*, entre outros.</p>
